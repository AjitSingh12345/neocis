package WireframeModel;

public class Point3D {
    public double x, y, z;

    /**
     * Point3D is a a type of point w/ x,y,z dimensions
     * @param x
     * @param y
     * @param z
     */
    public Point3D(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
